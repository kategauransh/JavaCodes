public class MultithreadingExample {
    public static void main(String[] args) {
        // Create a new thread that prints numbers from 1 to 5
        Thread myThread = new Thread(new NumberPrinter());
        myThread.start(); // Start the thread
    }
}

// Class implementing Runnable to define the thread's task
class NumberPrinter implements Runnable {
    @Override
    public void run() {
        for (int i = 1; i <= 5; i++) {
            System.out.println("Number: " + i); // Print the number
            try {
                Thread.sleep(1000); // Pause for 1 second
            } catch (InterruptedException e) {
                e.printStackTrace(); // Handle interruptions
            }
        }
    }
}